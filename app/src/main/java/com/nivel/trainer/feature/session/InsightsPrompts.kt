package com.nivel.trainer.feature.session

/**
 * Промпт-шаблон и пример формата для ручной вставки инсайтов (D2, #20).
 *
 * Порт один-в-один из веб-Nivel `src/lib/ai/insightsPrompt.ts`
 * (`INSIGHTS_PROMPT` / `INSIGHTS_FORMAT_EXAMPLE`): тренер копирует промпт в
 * Claude, вставляет markdown-ответ обратно в шит. Формат жёстко завязан на
 * серверный парсер `parseInsightsMarkdown` — менять синхронно с ним.
 */
object InsightsPrompts {

    /** Промпт, который тренер копирует и отдаёт Claude. Совпадает с вебом дословно. */
    const val PROMPT: String =
        "Ты помогаешь тренеру по паделю. Прочитай транскрипт тренировки, который приведён в следующем сообщении, и выдели практические инсайты для ученика.\n" +
            "\n" +
            "ВЫВЕДИ РЕЗУЛЬТАТ СТРОГО В ЭТОМ ФОРМАТЕ (никаких таблиц, нумерации, bold-выделений):\n" +
            "\n" +
            "## Карточка 1\n" +
            "- Тема: техника\n" +
            "- Заголовок: <короткий совет, до 80 знаков>\n" +
            "- Описание: <конкретное действие, до 400 знаков>\n" +
            "- Цитата: \"<дословная фраза из транскрипта>\"\n" +
            "\n" +
            "## Карточка 2\n" +
            "- Тема: тактика\n" +
            "- Заголовок: ...\n" +
            "- Описание: ...\n" +
            "- Цитата: \"...\"\n" +
            "\n" +
            "ПРАВИЛА:\n" +
            "- Тема обязательно одна из: техника, тактика, физика, менталка (строчные буквы)\n" +
            "- Минимум 5, максимум 15 карточек\n" +
            "- В каждой карточке цитата — дословная фраза из транскрипта (без неё инсайт невалиден)\n" +
            "- Только actionable советы, не пересказ\n" +
            "- Никакого markdown-форматирования внутри полей (**bold**, *italic* и т.п.)\n" +
            "- Никаких таблиц, нумерованных списков, заголовков # или ###"

    /** Пример ожидаемого формата для подсказки в шите. Совпадает с вебом дословно. */
    const val FORMAT_EXAMPLE: String =
        "## Карточка 1\n" +
            "- Тема: техника\n" +
            "- Заголовок: Поздний замах при бандеха\n" +
            "- Описание: Начинай замах когда мяч ещё летит — на 0.5 сек раньше.\n" +
            "- Цитата: \"ты опять поздно взял\"\n" +
            "\n" +
            "## Карточка 2\n" +
            "- Тема: тактика\n" +
            "- Заголовок: Держи позицию у сетки\n" +
            "- Описание: После удара смещайся к сетке, не оставайся у задней линии.\n" +
            "- Цитата: «не стой сзади, иди вперёд»"
}
